/**
 * Helper type that extracts the keys of a Zod schema as a union of string literals.
 *
 * @template T - Zod schema type.
 */
export type SchemaKeys<T extends z.ZodTypeAny> = keyof T["_output"];

/**
 * Deep partial version of any Zod-derived type.
 * Useful for patch updates where only a subset of fields is provided.
 *
 * @template T - Original type.
 */
export type DeepPartial<T> = {
  [P in keyof T]?: T[P] extends object ? DeepPartial<T[P]> : T[P];
};

/**
 * Export a consolidated object that groups all schemas.
 * This can be imported by modules that need runtime validation without pulling individual symbols.
 */
export const Schemas = {
  PromptMeta: PromptMetaSchema,
  GeneratedCode: GeneratedCodeSchema,
  Bundle: BundleSchema,
  PromptEdit: PromptEditSchema,
  GenerationOptions: GenerationOptionsSchema,
  PreviewResult: PreviewResultSchema,
} as const;

/**
 * Runtime guard that checks whether a given object conforms to the PromptMeta shape.
 *
 * @param obj - Object to test.
 * @returns `true` if the object matches PromptMeta, otherwise `false`.
 */
export function isPromptMeta(obj: unknown): obj is PromptMeta {
  return PromptMetaSchema.safeParse(obj).success;
}

/**
 * Runtime guard that checks whether a given object conforms to the GeneratedCode shape.
 *
 * @param obj - Object to test.
 * @returns `true` if the object matches GeneratedCode, otherwise `false`.
 */
export function isGeneratedCode(obj: unknown): obj is GeneratedCode {
  return GeneratedCodeSchema.safeParse(obj).success;
}

